package MathDemo;

public class MathDemo {
    public static void main(String[] args) {
        double x = Math.abs(-9.5);
        System.out.println(x);

        System.out.println(Math.abs(8.6));

        System.out.println(Math.sqrt(5.6));

        System.out.println(Math.pow(2,3));

        System.out.println(Math.PI);

        System.out.println(Math.log(3.0));

        System.out.println(Math.exp(1.0));

        System.out.println(Math.max(-2.3,4.5));

        System.out.println(Math.min(3.4,-4.2));

        System.out.println(Math.ceil(-5.6));

        System.out.println(Math.floor(-5.6));
    }
}
